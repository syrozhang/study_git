UINT8* GetString(UINT16 stringId)
{
    UINT8* str;
    UINT32 languageId = sPara.language;

    if (languageId >= LANGUAGES)
    {
		languageId = LanguageEnglish;
    }

    if(stringId >= MAX_ID) 
    {
		return 0;
    }

    if(UiStrings[stringId][languageId][0]=='\0')
    {
	    str = UiStrings[stringId][0];
    }
	else
	{
	    str = UiStrings[stringId][languageId];
	}

	return str;
}

